/** \brief La función sumarOperandos recibe dos valores de tipo float y devuelve el resultado tambien tipo float de la suma entre si mediante el return
 *
 * \param Es el primero de los operandos a sumar
 * \param Es el segundo de los operandos a sumar
 * \return Devuelve el resultado de la suma de los valores ingresados mediante el primer y segundo parametro.
 *
 */
float sumarOperandos(float, float);



/** \brief La funcion restarOperandos recibe dos valores de tipo float y devuelve el resultado tambien tipo float de la sustraccion del segundo parametro sobre el primero
 *
 * \param Es el primero de los operandos el cual va a ser restado por el segundo (minuendo)
 * \param Es el segundo de los operandos el cual va a restar al primero (sustraendo)
 * \return Devuelve el resultado de la diferencia entre el primer operando y el segundo tipo float
 *
 */
float restarOperandos(float, float);


/** \brief La funcion multiplicarOperandos recibe dos valores de tipo float y devuelve el producto de los mismos mediante el return de tipo float
 *
 * \param Es el primero de los operandos el cual va a ser multiplicado por el segundo (multiplicando)
 * \param Es el segundo de los operandos el cual va a multiplicar al primero (multiplicador)
 * \return Devuelve el resultado o producto de la multiplicacion entre los valores ingresados de tipo float
 *
 */
float multiplicarOperandos(float, float);


/** \brief La funcion dividirOperandos recibe dos valores de tipo float y devuelve el resto de los mismos mediante el return de tipo float
 *
 * \param Es el primero operando el cual va a ser dividido (dividendo)
 * \param Es el segundo operando el cual va a dividir al primero (divisor)
 * \return Devuelve el cociente de la division mediante el return de tipo float
 *
 */
float dividirOperandos(float, float);


/** \brief La funcion factorialNum recibe un valor de tipo float y devuelve el factorial del mismo numero
 *
 * \param Es el valor ingresado por el usuario del cual se pretende averiguar el factorial
 * \return Devuelve el factorial del numero ingresado
 *
 */
double factorialNum(double);
